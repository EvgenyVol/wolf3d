#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*cp_s1;

	cp_s1 = (char *)s1;
	while (*cp_s1)
		cp_s1++;
	while (*s2 && n--)
		*cp_s1++ = *s2++;
	*cp_s1++ = '\0';
	return (s1);
}
