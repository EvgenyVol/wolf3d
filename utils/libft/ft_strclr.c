#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	char	*cp_s;

	i = 0;
	cp_s = s;
	if (!s)
		return ;
	while (*cp_s++)
		i++;
	ft_memset(s, '\0', i);
}
